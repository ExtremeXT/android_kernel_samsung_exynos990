void dsp_ctrl_reg_print(unsigned int reg_id)
{
	unsigned int addr, count;

	dsp_enter();
	if (reg_id >= DSP_REG_END) {
		dsp_warn("register id is invalid(%u/%u)\n",
				reg_id, DSP_REG_END);
		return;
	}

	addr = sfr_reg[reg_id].base + sfr_reg[reg_id].offset;
	if (sfr_reg[reg_id].count) {
		for (count = 0; count < sfr_reg[reg_id].count; ++count) {
			dsp_notice("[%4d][%5x][%4x][%2x][%32s] %8x\n",
					reg_id, addr + count * 4,
					sfr_reg[reg_id].flag, count,
					sfr_reg[reg_id].name,
					dsp_ctrl_offset_readl(reg_id, count));
		}
	} else {
		dsp_notice("[%4d][%5x][%4x][--][%32s] %8x\n",
				reg_id, addr, sfr_reg[reg_id].flag,
				sfr_reg[reg_id].name, dsp_ctrl_readl(reg_id));
	}

	dsp_leave();
}

