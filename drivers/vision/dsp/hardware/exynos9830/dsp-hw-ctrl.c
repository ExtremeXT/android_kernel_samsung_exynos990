{
	struct dsp_ctrl *ctrl;

	dsp_enter();
	ctrl = &sys->ctrl;
	static_ctrl = ctrl;
	ctrl->sfr_pa = sys->sfr_pa;
	ctrl->sfr = sys->sfr;
	dsp_leave();
	return 0;
}

