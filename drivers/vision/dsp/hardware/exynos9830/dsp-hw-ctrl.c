int dsp_ctrl_common_init(struct dsp_ctrl *ctrl)
{
	dsp_enter();
	/* change secure flag */
	dsp_ctrl_writel(DSPC_SEC_MEMORY_FLAG, 0xf5);
	dsp_ctrl_writel(DSPC_SEC_SLAVE_FLAG, 0x7d1);

	/* remap */
	dsp_ctrl_writel(DSPC_CPU_REMAPS1, 0x1);
	dsp_ctrl_writel(DSPC_CPU_REMAPD1, 0x40340000);

	dsp_leave();
	return 0;
}

int dsp_ctrl_init(struct dsp_ctrl *ctrl)
{
	dsp_enter();
	dsp_ctrl_writel(DSPC_MCGEN, 0xfffcffff);

	/*
	 * cache coherence
	 * Cache AXI write transaction
	 * [2:0] cache hint (LLC) : 0x000
	 * [3] Full line info : 0x1 (WLU) / 0x0 (WU)
	 * [5:4] Data share type : 0x1 (share with CPU)
	 * Cache AXI read transaction
	 * [10:8] cache hint (LLC) : 0x000
	 * [13:12] Data share type : 0x1 (share with CPU)
	 */
	dsp_ctrl_writel(DSPC_M2_AXUSER, 0x1010);

	/* enable debug monitor */
	dsp_ctrl_writel(DSPC_DBG_INTR_ENABLE, 0xffffffff);
	dsp_ctrl_writel(DSPC_DBG_DINTR_ENABLE, 0xffffffff);
	dsp_ctrl_writel(DSPC_DBG_MON_ENABLE, 0x3);
	dsp_ctrl_writel(DSP0_DBG_MON_ENABLE, 0xf);
	dsp_ctrl_writel(DSP0_DBG_INTR_ENABLE, 0x3ffe);
	dsp_ctrl_writel(DSP1_DBG_MON_ENABLE, 0xf);
	dsp_ctrl_writel(DSP1_DBG_INTR_ENABLE, 0x3ffe);
	dsp_ctrl_writel(DSP2_DBG_MON_ENABLE, 0xf);
	dsp_ctrl_writel(DSP2_DBG_INTR_ENABLE, 0x3ffe);
	dsp_leave();
	return 0;
}

int dsp_ctrl_all_init(struct dsp_ctrl *ctrl)
{
	dsp_enter();
	dsp_ctrl_common_init(ctrl);
	dsp_ctrl_init(ctrl);
	dsp_leave();
	return 0;
}

int dsp_ctrl_start(struct dsp_ctrl *ctrl)
{
	unsigned int release;

	dsp_enter();
	release = dsp_ctrl_readl(DSPC_CPU_RELEASE);
	if (release & 0x2)
		dsp_ctrl_writel(DSPC_CPU_RELEASE, release | 0x5);
	else
		dsp_ctrl_writel(DSPC_CPU_RELEASE, 0x5);

	dsp_leave();
	return 0;
}

int dsp_ctrl_reset(struct dsp_ctrl *ctrl)
{
	int ret;
	unsigned int wfi, wfe, release;
	unsigned int repeat = 1000;

	dsp_enter();
	while (1) {
		wfi = dsp_ctrl_readl(DSPC_CPU_WFI_STATUS);
		wfe = dsp_ctrl_readl(DSPC_CPU_WFE_STATUS);
		if (wfi & 0x1 || wfe & 0x1 || !repeat)
			break;
		repeat--;
		udelay(100);
	};

	if (!(wfi & 0x1 || wfe & 0x1)) {
		dsp_err("status of device is not idle(%#x/%#x)\n", wfi, wfe);
		ret = -ETIMEDOUT;
		dsp_dump_ctrl();
		goto p_err;
	}

	release = dsp_ctrl_readl(DSPC_CPU_RELEASE);
	if (release & 0x2)
		dsp_ctrl_writel(DSPC_CPU_RELEASE, release & ~0x1);
	else
		dsp_ctrl_writel(DSPC_CPU_RELEASE, 0x0);

	dsp_leave();
	return 0;
p_err:
	return ret;
}

